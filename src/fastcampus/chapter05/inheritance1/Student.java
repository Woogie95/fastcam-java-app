package fastcampus.chapter05.inheritance1;

public class Student {
    String name;
    int score;

    String getName() {
        return name;
    }

    void setName(String name) {
        this.name = name;
    }

    int getScore() {
        return score;
    }

    void setScore(int score) {
        this.score = score;
    }
}
